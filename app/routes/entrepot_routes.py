from flask import Blueprint, render_template, jsonify, current_app

entrepot_bp = Blueprint('entrepot', __name__)

# ====================================================
# Route pour afficher l'int√©rieur d'un entrep√¥t sp√©cifique
# ====================================================
@entrepot_bp.route("/entrepot/<nom>")
def afficher_entrepot(nom):
    """
    Affiche l'int√©rieur d'un entrep√¥t sp√©cifique √† partir des donn√©es extraites du fichier Excel.
    
    Param√®tres:
        nom (str): Le nom de l'entrep√¥t, utilis√© pour d√©terminer le nom de la feuille Excel.
    
    Retour:
        Rendu du template 'entrepot.html' avec les √©l√©ments et murs extraits,
        ou une page d'erreur si le fichier ou la feuille est introuvable ou incomplet.
    """
    # R√©cup√©rer le DataFrame Excel stock√© dans la configuration de l'application
    df_excel = current_app.config.get("df_excel")
    if df_excel is None:
        print(f"‚ö†Ô∏è ERREUR : df_excel est None, impossible d'afficher {nom}")
        return render_template("erreur.html", message="Fichier Excel introuvable."), 500

    # D√©terminer le nom de la feuille correspondant √† l'entrep√¥t (conversion en minuscules, espaces remplac√©s par _)
    feuille = nom.lower().replace(" ", "_")
    print(f"üìå Recherche de la feuille Excel : {feuille}")
    print(f"üìÑ Feuilles disponibles : {list(df_excel.keys())}")

    if feuille not in df_excel:
        print("‚ö†Ô∏è ERREUR : La feuille Excel n'existe pas !")
        return render_template("erreur.html", message=f"Aucun plan disponible pour l'entrep√¥t {nom}."), 404

    df_interieur = df_excel[feuille]

    # D√©finir les colonnes obligatoires pour extraire les √©l√©ments et les murs
    required_columns_elements = {"Value", "Position X", "Position Y"}
    required_columns_murs = {"Start X", "Start Y", "End X", "End Y"}

    # V√©rifier que les colonnes n√©cessaires pour les √©l√©ments existent
    missing_elements = required_columns_elements - set(df_interieur.columns)
    if missing_elements:
        print(f"‚ö†Ô∏è ERREUR : Colonnes manquantes pour les √©l√©ments : {missing_elements}")
        return render_template("erreur.html", message="Colonnes des √©l√©ments manquantes dans le fichier Excel."), 500

    # V√©rifier que les colonnes n√©cessaires pour les murs existent
    missing_murs = required_columns_murs - set(df_interieur.columns)
    if missing_murs:
        print(f"‚ö†Ô∏è ERREUR : Colonnes manquantes pour les murs : {missing_murs}")
        return render_template("erreur.html", message="Colonnes des murs manquantes dans le fichier Excel."), 500

    # Extraction des donn√©es pour les √©l√©ments (emplacements) et les murs
    entrepot_data = df_interieur.dropna(subset=["Value", "Position X", "Position Y"])[["Value", "Position X", "Position Y"]].to_dict(orient="records")
    murs_data = df_interieur.dropna(subset=["Start X", "Start Y", "End X", "End Y"])[["Start X", "Start Y", "End X", "End Y"]].to_dict(orient="records")

    print(f"üìå {len(entrepot_data)} √©l√©ments et {len(murs_data)} murs charg√©s.")

    # Rendu du template 'entrepot.html' en passant le nom, les √©l√©ments et les murs
    return render_template("entrepot.html", entrepot=nom, elements=entrepot_data, murs=murs_data)
